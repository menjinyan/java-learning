package src.menjinyan.Demo;

public class Demo06比较运算符 {
    public static void main(String[] args) {
        /* 比较运算符*/
        //1、判断左右两边是否相等
        int a=10;
        int b=10;
        int c=20;
        System.out.println(a==b);
        System.out.println(a==c);

        //2、判断左右两边是否不相等
        int d=10;
        int e=10;
        int f=20;
        System.out.println(d!=e);
        System.out.println(d!=f);
    }
}
